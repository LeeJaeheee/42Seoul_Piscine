#include <unistd.h>

void	print_c(char *c)
{
	while (c[3] <= '9')
	{
		while (c[4] <= '9')
		{
			write(1, c, 5);
			if (!(c[0] == '9' && c[1] == '8'))
				write(1, ", ", 2);
			c[4]++;
		}
		c[4] = '0';
		c[3]++;
	}
}

void	ft_print_comb2(void)
{
	char	c[5];

	c[0] = '0';
	c[2] = ' ';
	while (c[0] <= '9')
	{
		c[1] = '0';
		while (c[1] <= '9')
		{
			c[3] = c[0];
			c[4] = c[1] + 1;
			print_c(c);
			c[1]++;
		}
		c[0]++;
	}
}
